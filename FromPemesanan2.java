    private void loadData() {
    try {
        java.sql.Connection conn = koneksiDB.getConnection();
        java.sql.ResultSet rs = conn.createStatement().executeQuery("SELECT * FROM pemesanan");
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("namaPelanggan"),
rs.getString("noTelepon"),
rs.getString("ruangan"),
rs.getString("tanggalMasuk"),
rs.getString("tanggalKeluar"),
rs.getString("totalHari"),
rs.getString("totalHarga")
            });
        }
        conn.close();
    } catch (Exception e) {
        System.out.println("Error loadData: " + e.getMessage());
    }
}

