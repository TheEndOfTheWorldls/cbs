    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int row = jTable1.getSelectedRow();
if (row == -1) {
    javax.swing.JOptionPane.showMessageDialog(this, "Pilih data di tabel dulu!");
    return;
}
int konfirmasi = javax.swing.JOptionPane.showConfirmDialog(this, "Yakin hapus?", "Konfirmasi", javax.swing.JOptionPane.YES_NO_OPTION);
if (konfirmasi == javax.swing.JOptionPane.YES_OPTION) {
    try {
        java.sql.Connection conn = koneksiDB.getConnection();
        java.sql.PreparedStatement ps = conn.prepareStatement ("DELETE FROM pemesanan WHERE namaPelanggan=?");
        ps.setString(1, jTable1.getValueAt(row, 0).toString());
        ps.executeUpdate();
        javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
        loadData();
        conn.close();
    } catch (Exception e) {
        System.out.println("Error: " + e.getMessage());
    }
}
    }//GEN-LAST:event_jButton3ActionPerformed

